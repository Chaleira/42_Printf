#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	i;
	char	*str;

	i = n;
	str = "0123456789";
	if (i < 0)
	{
		write(fd, "-", 1);
		i = i * -1;
	}
	if (i >= 10)
		ft_putnbr_fd(i / 10, fd);
	ft_putchar_fd(str[i % 10], fd);
}
